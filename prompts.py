# ==================== 辅助函数：填充 Prompt ====================
def fill_prompt(template, **kwargs):
    """
    使用 str.replace() 方法安全地替换模板中的占位符。
    占位符格式为 {变量名}，例如 {question}。
    """
    filled = template
    for key, value in kwargs.items():
        placeholder = "{" + key + "}"
        filled = filled.replace(placeholder, str(value))
    return filled